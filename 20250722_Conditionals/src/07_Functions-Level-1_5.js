function math(val1, val2) {
  let result = val1 * val2

  return console.log(val1 + " * " + val2 + " = " + result)
}

math("30", "20")
math("100", "100")
math("5", "0")
math("45", "173")
math("1", "1000")

function math2(val1, val2) {
  let result = val1 / val2
  return console.log(val1 + " / " + val2 + " = " + result)
}

math2("30", "20")
math2("100", "100")
math2("5", "0")
math2("45", "173")
math2("1", "1000")
